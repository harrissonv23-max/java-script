// soma 
var soma = a + b;
console.log(soma);

// subtração
var subtracao = a - b;
console.log(subtracao);

// multiplicação
var multiplicacao = a * b;
console.log(multiplicacao);

// divisão
var divisao = a / b;
console.log(divisao);

// operador de módulo
var modulo = a % b;
console.log(modulo);

